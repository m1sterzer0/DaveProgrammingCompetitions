type segtree struct { n, size, log int; op func(int64, int64) int64; e int64; d []int64 }
func Newsegtree(n int, op func(int64, int64) int64, e int64) *segtree {
	v := make([]int64, n); for i := 0; i < n; i++ { v[i] = e }; return NewsegtreeVec(v, op, e)
}
func NewsegtreeVec(v []int64, op func(int64, int64) int64, e int64) *segtree {
	n, sz, log := len(v), 1, 0; for sz < n { sz <<= 1; log += 1 }; d := make([]int64, 2*sz); d[0] = e
	for i := 0; i < n; i++ { d[sz+i] = v[i] }; st := &segtree{n, sz, log, op, e, d}
	for i := sz - 1; i >= 1; i-- { st.update(i) }; return st
}
func (q *segtree) Set(p int, v int64) {
	p += q.size; q.d[p] = v; for i := 1; i <= q.log; i++ { q.update(p >> uint(i)) }
}
func (q *segtree) Get(p int) int64 { return q.d[p+q.size] }
func (q *segtree) Prod(l int, r int) int64 {
	if r < l { return q.e }; r += 1; sml, smr := q.e, q.e; l += q.size; r += q.size
	for l < r {
		if l&1 != 0 { sml = q.op(sml, q.d[l]); l++ }; if r&1 != 0 { r--; smr = q.op(q.d[r], smr) }; l >>= 1; r >>= 1
	}
	return q.op(sml, smr)
}
func (q *segtree) Allprod() int64 { return q.d[1] }
func (q *segtree) MaxRight(l int, f func(int64) bool) int {
	if l == q.n { return q.n - 1 }; l += q.size; sm := q.e
	for {
		for l%2 == 0 { l >>= 1 }
		if !f(q.op(sm, q.d[l])) {
			for l < q.size { l *= 2; if f(q.op(sm, q.d[l])) { sm = q.op(sm, q.d[l]); l++ } }; return l - q.size - 1
		}
		sm = q.op(sm, q.d[l]); l++; if l&-l == l { break }
	}
	return q.n - 1
}
func (q *segtree) MinLeft(r int, f func(int64) bool) int {
	if r < 0 { return 0 }; r += q.size; sm := q.e; r++ 
	for {
		r--; for r > 1 && r%2 == 1 { r >>= 1 }
		if !f(q.op(q.d[r], sm)) {
			for r < q.size { r = 2*r + 1; if f(q.op(q.d[r], sm)) { sm = q.op(q.d[r], sm); r-- } }; return r + 1 - q.size
		}
		sm = q.op(q.d[r], sm); if r&-r == r { break }
	}
	return 0
}
func (q *segtree) update(k int) { q.d[k] = q.op(q.d[2*k], q.d[2*k+1]) }

type stack struct { buf []xxx; l int }
func Newstack() *stack { buf := make([]xxx, 0); return &stack{buf, 0} }
func (q *stack) IsEmpty() bool { return q.l == 0 }
func (q *stack) Clear() { q.buf = q.buf[:0]; q.l = 0 }
func (q *stack) Len() int { return q.l }
func (q *stack) Push(x xxx) { q.buf = append(q.buf, x); q.l++ }
func (q *stack) Pop() xxx {
	if q.l == 0 { panic("Empty stack Pop()") }; v := q.buf[q.l-1]; q.l--; q.buf = q.buf[:q.l]; return v
}
func (q *stack) Head() xxx { if q.l == 0 { panic("Empty stack Head()") }; return q.buf[q.l-1] }
func (q *stack) Top() xxx { return q.Head() }

type xxx struct { i,v int }

func main() {
	//f1, _ := os.Create("cpu.prof"); pprof.StartCPUProfile(f1); defer pprof.StopCPUProfile()
	defer wrtr.Flush()
	infn := ""; if infn == "" && len(os.Args) > 1 {	infn = os.Args[1] }
	if infn != "" {	f, e := os.Open(infn); if e != nil { panic(e) }; rdr = bufio.NewScanner(f) }
	rdr.Split(bufio.ScanWords); rdr.Buffer(make([]byte,1024),1_000_000_000)
	// PROGRAM STARTS HERE
	T := gi()
	for tt:=1;tt<=T;tt++ {
		N := gi(); A := gis(N)
		cum := make([]int64,N+1); cum[0] = 0; for i:=0;i<N;i++ { cum[i+1] = cum[i] + int64(A[i])  } // Add 1 to deal with zero on left
		// Just use a segtree for RMQs -- lazy
		stmin := NewsegtreeVec(cum,min64 ,1000000000000000000)
		stmax := NewsegtreeVec(cum,max64,-1000000000000000000)
		// Use stack algo to find the intervals for which the given number is at least tied with maximum
		larr,rarr := ia(N),ia(N)
		st := Newstack()
		for i:=0;i<N;i++ {
			for !st.IsEmpty() && st.Head().v <= A[i] { st.Pop() }
			if st.IsEmpty() { larr[i] = 0 } else { larr[i] = st.Head().i+1 }
			st.Push(xxx{i,A[i]})
		}
		st.Clear()
		for i:=N-1;i>=0;i-- {
			for !st.IsEmpty() && st.Head().v <= A[i] { st.Pop() }
			if st.IsEmpty() { rarr[i] = N-1 } else { rarr[i] = st.Head().i-1 }
			st.Push(xxx{i,A[i]})

		}
		// Now for the queries
		good := true
		for i,a := range A {
			lsum := stmin.Prod(larr[i],i)
			rsum := stmax.Prod(i+1,rarr[i]+1)
			if rsum-lsum > int64(a) { good = false; break }
		}
		if good { fmt.Fprintln(wrtr,"YES") } else { fmt.Fprintln(wrtr,"NO") }
	}
}

