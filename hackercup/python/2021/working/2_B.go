func main() {
	//f1, _ := os.Create("cpu.prof"); pprof.StartCPUProfile(f1); defer pprof.StopCPUProfile()
    defer wrtr.Flush()
	infn := ""
	if infn == "" && len(os.Args) > 1 {	infn = os.Args[1] }
    if infn != "" {	f, e := os.Open(infn); if e != nil { panic(e) }; rdr = newScanner(f) }
	T := gi()
	for tt:=1; tt <= T; tt++ {
		N := gi(); A,B := fill2(N-1); for i:=0;i<N-1;i++ { A[i]--; B[i]-- }; F := gis(N)
		//fmt.Fprintf(os.Stderr,"Case #%v N=%v A=%v B=%v F=%v\n",tt,N,A,B,F)
		fmt.Fprintf(os.Stderr,"Case #%v N=%v\n",tt,N)

		fcnt := make(map[int]int)
		for _,f := range F { fcnt[f]++ }
		ans := 0
		gr := make([][]int,N)
		for i:=0;i<N-1;i++ { a,b := A[i],B[i]; gr[a] = append(gr[a],b); gr[b] = append(gr[b],a) }
		var dfs func(int,int) map[int]int
		dfs = func(n,p int) map[int]int {
			res := make(map[int]int)
			res[F[n]]++
			if res[F[n]] == fcnt[F[n]] { delete(res,F[n]) }
			for _,c := range gr[n] {
				if c == p { continue }
				res2 := dfs(c,n)
				if len(res2) > len(res) { res,res2 = res2,res }
				for k,v := range res2 {
					res[k] += v; if res[k] == fcnt[k] { delete(res,k) }
				}
			}
			if len(res) == 0 && n != 0{ ans++ }
			return res
		}
		dfs(0,-1)
		fmt.Printf("Case #%v: %v\n",tt,ans)
	}
}



