func zeroarr(a []int) { for i:=0; i<len(a); i++ { a[i] = 0 } }
func powmod(a,e,mod int) int { res, m := 1, a; for e > 0 { if e&1 != 0 { res = res * m % mod }; m = m * m % mod; e >>= 1 }; return res }
func powint(a,e int) int { res, m := 1, a; for e > 0 { if e&1 != 0 { res = res * m }; m = m * m; e >>= 1 }; return res }
func gcd(a,b int) int { for b != 0 { t:=b; b=a%b; a=t }; return a }
func gcdExtended(a,b int) (int,int,int) { if a == 0 { return b,0,1 }; gcd,x1,y1 := gcdExtended(b%a,a); return gcd, y1-(b/a)*x1,x1 }
func modinv(a,m int) (int,bool) { g,x,_ := gcdExtended(a,m); if g != 1 { return 0,false }; return (x % m + m) % m,true  }
func vecintstring(a []int) string { astr := make([]string,len(a)); for i,a := range a { astr[i] = strconv.Itoa(a) }; return strings.Join(astr," ") }
func makefact(n int,mod int) ([]int,[]int) {
	fact,factinv := make([]int,n+1),make([]int,n+1)
	fact[0] = 1; for i:=1;i<=n;i++ { fact[i] = fact[i-1] * i % mod }
	factinv[n] = powmod(fact[n],mod-2,mod); for i:=n-1;i>=0;i-- { factinv[i] = factinv[i+1] * (i+1) % mod }
	return fact,factinv
}
const MOD = 998244353
func main() {
	//f1, _ := os.Create("cpu.prof"); pprof.StartCPUProfile(f1); defer pprof.StopCPUProfile()
	defer wrtr.Flush()
	infn := ""; if infn == "" && len(os.Args) > 1 {	infn = os.Args[1] }
	if infn != "" {	f, e := os.Open(infn); if e != nil { panic(e) }; rdr = bufio.NewScanner(f) }
	rdr.Split(bufio.ScanWords); rdr.Buffer(make([]byte,1024),1_000_000_000)
	// PROGRAM STARTS HERE
	N,D := gi(),gi()
	fact,factinv := makefact(20000,MOD)
	comb := func(n,r int) int { if n < 0 || r < 0 || r > n { return 0 }; return fact[n] * factinv[r] % MOD * factinv[n-r] % MOD }
	sqMatMul := func(n int, a1,a2,dst [][]int) {
		for i:=0;i<n;i++ {
			for j:=0;j<n;j++ {
				dst[i][j] = 0
				for k:=0;k<n;k++ { dst[i][j] += a1[i][k] * a2[k][j] % MOD }
				dst[i][j] %= MOD
			}
		}
	}
	sqMatExp := func(n int, a [][]int, expval int) [][]int {
		b := twodi(n,n,0)
		nb := twodi(n,n,0)
		res := twodi(n,n,0)
		newres := twodi(n,n,0)
		for i:=0;i<n;i++ { res[i][i] = 1 }
		for i:=0;i<n;i++ { for j:=0;j<n;j++ { b[i][j] = a[i][j] } }
		for expval > 0 {
			if expval & 1 == 1 { sqMatMul(n,b,res,newres); res,newres = newres,res }
			expval = expval >> 1
			sqMatMul(n,b,b,nb); b,nb = nb,b
		}
		return res
	}

	A := twodi(2,2,0)
	ans := 0
	for i:=0;i<=D+1;i++ { // Iterating over number of white stones
		// Build a matrix
		// [ a11  a12 ] [num ways starting on black  ] == [num ways ending on black]
		// [ a21  a22 ] [num ways starting on white  ]    [num ways ending on white]
		A[0][0] = comb(D-1,i)
		A[0][1] = comb(D-1,i-1)
		A[1][0] = comb(D-1,i-1)
		A[1][1] = comb(D-1,i-2)
		B := sqMatExp(2,A,N)
		ans += B[0][0] + B[1][1]
	}
	ans %= MOD
	fmt.Println(ans)
}
